#include <iostream>  // Include the input-output stream library

// Main function - entry point of the program

// "<<" this operator is known as insertion operator in c++

int main() {
    // Single line comment

    /*
        Multiline comments
    */

    // Code goes here
    std::cout << "Hello, World!" << std::endl;  // Print "Hello, World!" to the consoles
    // "endl" is denotes to end the line and print anaother line in new line.

    printf("Hello world by printf ");

    return 0;  // Return 0 to indicate the program ended successfully
}


